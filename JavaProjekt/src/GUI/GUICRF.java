    private boolean provjeraSlova(String unos, JLabel label) {

        if (unos.isEmpty()) {
            label.setText("*");
            return false;
        }
        for (char c : unos.toCharArray()) {
            if (Character.isDigit(c)) {
                label.setText("**");
                return false;
            }
        }
        label.setText("");

        return true;
    }

    private boolean provjeraBrojeva(String unos, JLabel label) {

        if (unos.isEmpty()) {
            label.setText("*");
            return false;
        }
        for (char c : unos.toCharArray()) {
            if (!Character.isDigit(c)) {
                label.setText("**");
                return false;
            }
        }
        label.setText("");
        return true;
    }

    private String unesiString(String unos, JLabel label) {
        if (provjeraSlova(unos, label)) {
            return unos;
        }
        return null;

    }

    private int unesiBroj(String unos, JLabel label) {
        int broj = 0;
        if (provjeraBrojeva(unos, label)) {
            broj = Integer.parseInt(unos);
        }
        return broj;

    }

    private float unesiFloat(String unos, JLabel label) {
        float broj = 0;
        try {
            broj = Float.parseFloat(unos);
        } catch (Exception e) {
            label.setText("**");
        }
        if (unos == null) {
            label.setText("*");
        }
        label.setText("");
        return broj;

    }

    private String unesiTelefon(String unos, JLabel label) {

        if (provjeraBrojeva(unos, label)) {
            return unos;
        }
        return null;

    }
