    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String IDDoktora = txtIDDoktora.getText();
        int idDoktora = 0;
        boolean doktor = true;
        boolean datum = true;
        try {
            idDoktora = Integer.parseInt(IDDoktora);
        } catch (Exception e) {
            kriviDoktor.setText("Krivi unos.");
            doktor = false;
        }

        if (txtIDDoktora.getText().isEmpty()) {
            kriviDoktor.setText("Prazno polje.");
            doktor = false;
        }
        if (doktor) {
            kriviDoktor.setText("");
        }
        String IDPacijenta = txtIDPacijenta.getText();
        int idPacijenta = 0;
        boolean pacijent = true;
        try {
            idPacijenta = Integer.parseInt(IDPacijenta);
        } catch (Exception e) {
            kriviPacijent.setText("Krivi unos.");
            pacijent = false;
        }

        if (txtIDDoktora.getText().isEmpty()) {
            kriviPacijent.setText("Prazno polje.");
            pacijent = false;
        }

        if (pacijent) {
            kriviPacijent.setText("");
        }

        DateFormat format = new SimpleDateFormat("dd.MM.yyyy.");
        Date date = null;
        try {
            LocalDate dateLocal = LocalDate.parse(txtDate.getText(), DateTimeFormatter.ofPattern("dd.MM.yyyy."));
            LocalDate danasnji = LocalDate.now();
            if (dateLocal.isBefore(danasnji)) {
                greskaDatum.setText("Unesite datum u buduÄ‡nosti.");
                datum = false;
            }
            date = Date.valueOf(dateLocal);
        } catch (Exception ex) {
            greskaDatum.setText("Datum je u krivom formatu. (dd.MM.yyyy.)");
            datum = false;
        }
        if (datum) {
            greskaDatum.setText("");
        }

        if (!valid.provjeriIDDoktora(idDoktora)) {
            kriviDoktor.setText("Krivi id.");
        }

        if (!valid.provjeriIDPacijenta(idPacijenta)) {
            kriviPacijent.setText("Krivi id.");
        }

        if (pacijent && doktor && datum) {

            if (valid.provjeriIDDoktora(idDoktora) && valid.provjeriIDPacijenta(idPacijenta)) {
                Appointment app = new Appointment(date, idDoktora, idPacijenta);
                upis.unesiAppointment(app);

                labelRezultat.setText("Uspjesno");
                this.dispose();
            } else {

                labelRezultat.setText("Neuspjesno");
            }

        } else {
            labelRezultat.setText("Neuspjesno");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

